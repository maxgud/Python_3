#just an array of animals
animals = ['bear', 'python3.6', 'peacock', 'kangaroo', 'whale', 'platypus']

#both of these for loops have the same results
#using different methods
#the first just prints the value of each element in
#the array animals
for i in animals:
	print(i)

#This for loop gets the numbers 0-5 and identifies
#each element in the array animals and prints it out
for i in range (0,6):
	print(animals[i])